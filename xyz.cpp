#include <iostream>
using namespace std;
int main(void){
    int a = 57;
     cout << " the post increment operator is  " <<a++<<endl;
     cout << " the pre increment operator is  " <<++a<<endl;
     cout << " the no. on increasing by 1 is  " <<a + 1<<endl;
     cout << " the post deccrement operator is  " <<a--<<endl;
     cout << " the pre increment operator is  " <<--a<<endl;
     cout << " the no. on deccreasing by 1 is  " <<a - 1<<endl;
   
    
}
